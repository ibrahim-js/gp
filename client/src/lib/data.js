// Fonction pour formater le texte avec des retours √† la ligne apr√®s 7 mots
export function formatText(text) {
  if (!text) return "";

  // Diviser le texte en mots
  const words = text.split(" ");

  // Regrouper les mots par groupes de 7 (ou moins pour le dernier groupe)
  const chunks = [];
  for (let i = 0; i < words.length; i += 7) {
    chunks.push(words.slice(i, i + 7).join(" "));
  }

  // Joindre les groupes avec des retours √† la ligne
  return chunks.join("\n");
}

